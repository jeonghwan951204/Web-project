package com.exam.project01;

public class PlayerTO {
	private String playerName;
	private String playerGame;
	private String playerTime;
	private String playerGoal;
	private String playerAssist;
	private String playerSave;
	private String playerTeam;
	public String getPlayerName() {
		return playerName;
	}
	public void setPlayerName(String playerName) {
		this.playerName = playerName;
	}
	public String getPlayerGame() {
		return playerGame;
	}
	public void setPlayerGame(String playerGame) {
		this.playerGame = playerGame;
	}
	public String getPlayerTime() {
		return playerTime;
	}
	public void setPlayerTime(String playerTime) {
		this.playerTime = playerTime;
	}
	public String getPlayerGoal() {
		return playerGoal;
	}
	public void setPlayerGoal(String playerGoal) {
		this.playerGoal = playerGoal;
	}
	public String getPlayerAssist() {
		return playerAssist;
	}
	public void setPlayerAssist(String playerAssist) {
		this.playerAssist = playerAssist;
	}
	public String getPlayerSave() {
		return playerSave;
	}
	public void setPlayerSave(String playerSave) {
		this.playerSave = playerSave;
	}
	public String getPlayerTeam() {
		return playerTeam;
	}
	public void setPlayerTeam(String playerTeam) {
		this.playerTeam = playerTeam;
	}
	
}
